    public boolean isEmptyStudent() {
        if (ipt_studentName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Name is missing");
            return false;
        }
        if (chose_dateOfBirth.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Day of birth is missing");
            return false;
        }
        if (chose_dateOfBirth.getDate().compareTo(new Date()) > 0) {
            JOptionPane.showMessageDialog(this, "There ain't be no time traveller");
            return false;
        }
        if (ipt_studentPhoneNumber.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Phone number is missing");
            return false;
        }
        if (ipt_studentPhoneNumber.getText().length() != 10) {
            JOptionPane.showMessageDialog(this, "Invalid phone number");
            return false;
        }
        if (!ipt_studentEmail.getText().matches("^[\\w-\\.]+@([\\w-]+\\.)+[\\w-]{2,4}$")) {
            JOptionPane.showMessageDialog(this, "Invalid email address");
            return false;
        }
        if (ipt_studentAddress.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Address is missing");
            return false;
        }
        return true;
    }

