    private void btn_find_student_for_infoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_find_student_for_infoActionPerformed
        if (search_student_for_info.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Must enter student's id");
        } else {
            String maSV = search_student_for_info.getText().toString();
            if (student.check_maSV(maSV)) {
                student_info_table.setModel(new DefaultTableModel(null, new Object[]{"Student ID", "Name", "Date of birth", "Gender", "Phone number", "Email", "Address"}));
                student.getStudentInfo_by_maSV(student_info_table, maSV);
            } else {
                JOptionPane.showMessageDialog(this, "Id does not exist");
                search_student_for_info.requestFocus();
            }
        }
    }//GEN-LAST:event_btn_find_student_for_infoActionPerformed

