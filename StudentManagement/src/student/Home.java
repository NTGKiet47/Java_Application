    private void student_info_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_student_info_tableMouseClicked
        model = (DefaultTableModel) student_info_table.getModel();
        rowIndex = student_info_table.getSelectedRow();
        ipt_studentID.setText(model.getValueAt(rowIndex, 0).toString());
        ipt_maSV.setText(model.getValueAt(rowIndex, 1).toString());
        ipt_studentName.setText(model.getValueAt(rowIndex, 2).toString());
        Date date;
        try {
            date = new SimpleDateFormat("dd-MM-yyyy").parse(model.getValueAt(rowIndex, 3).toString());
            chose_dateOfBirth.setDate(date);
        } catch (ParseException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }
        String gender = model.getValueAt(rowIndex, 4).toString();
        if (gender.equals("Male")) {
            compb_gender.setSelectedIndex(0);
        } else if (gender.equals("Female")) {
            compb_gender.setSelectedIndex(1);
        }
        ipt_studentPhoneNumber.setText(model.getValueAt(rowIndex, 5).toString());
        ipt_studentEmail.setText(model.getValueAt(rowIndex, 6).toString());
        ipt_studentAddress.setText(model.getValueAt(rowIndex, 7).toString());
    }//GEN-LAST:event_student_info_tableMouseClicked

